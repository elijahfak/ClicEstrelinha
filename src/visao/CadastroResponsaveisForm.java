    public void popular_campos() {
        if (responsavel_em_cadastro != null) {
            this.campoNome.setText(responsavel_em_cadastro.getNome());
            this.campoCPF.setText(String.valueOf(responsavel_em_cadastro.getCpf()));
            
            this.campoCPF.setEditable(false);
            
            this.campoTelefone.setText(String.valueOf(responsavel_em_cadastro.getTelefone()));
            this.campoEmail.setText(responsavel_em_cadastro.getEmail());

            if (responsavel_em_cadastro.getSexo() == 1) {
                sexom.setSelected(true);
                sexof.setSelected(false);
            } else {
                sexom.setSelected(false);
                sexof.setSelected(true);
            }
        } else {
            this.campoNome.setText("");
            this.campoCPF.setText("");
            this.campoTelefone.setText("");
            this.campoEmail.setText("");

            sexom.setSelected(true);
            sexof.setSelected(false);
        }

    }

