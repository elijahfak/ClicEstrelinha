    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        boolean sexo_m = sexom.isSelected();
        try {
            if (responsavel_em_cadastro != null) {
                this.controle.editarResponsavel(
                        responsavel_em_cadastro,
                        this.campoNome.getText(),
                        this.campoCPF.getText(),
                        this.campoTelefone.getText(),
                        this.campoEmail.getText(),
                        sexo_m ? 1 : 0
                );
            } else {
                this.controle.cadastrarResponsavel(
                        this.campoNome.getText(),
                        this.campoCPF.getText(),
                        this.campoTelefone.getText(),
                        this.campoEmail.getText(),
                        sexo_m ? 1 : 0
                );
            }
            
            dispose();
        } catch (PreenchimentoCamposException ex) {
            PopUps.showMessage(ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

