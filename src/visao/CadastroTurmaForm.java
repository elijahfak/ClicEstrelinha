    public void popular_campos() {
        if (turma_em_edicao == null) {
            nomeCampo.setText("");
            salaCampo.setText("");
        } else {
            nomeCampo.setText(turma_em_edicao.getNome());
            salaCampo.setText(String.valueOf(turma_em_edicao.getSala()));

            int educadorIndex = controle.getFuncionarioIndicePorCpf(3, turma_em_edicao.getEducador().getCpf());

//            System.out.println(">>>>>>>>> " + educadorIndex + " >> " + turma_em_edicao.getEducador().getNome());
            educadorCampo.setSelectedIndex(educadorIndex);

            int auxiliarIndex = controle.getFuncionarioIndicePorCpf(4, turma_em_edicao.getAuxiliar().getCpf());
            auxiliarCampo.setSelectedIndex(auxiliarIndex);

            tipoCampo.setSelectedIndex(turma_em_edicao.getTipo());
            turnoCampo.setSelectedIndex(turma_em_edicao.getTurno());

//            System.out.println(turma_em_edicao.getAlunos().toString());
            for (int i = 0; i < alunoCampo.getRowCount(); i++) {
                String mat = alunoCampo.getValueAt(i, 0).toString();
//                System.out.println(mat);

                if (turma_em_edicao.isAlunoEmTurma(mat)) {
                    alunoCampo.getSelectionModel().addSelectionInterval(i, i);
                }
            }
        }
    }

