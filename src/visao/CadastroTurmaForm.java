    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome = nomeCampo.getText();
        int indice_educador = educadorCampo.getSelectedIndex();
        int indice_auxiliar = auxiliarCampo.getSelectedIndex();
        int tipo = tipoCampo.getSelectedIndex();
        String sala = salaCampo.getText();
        int turno = turnoCampo.getSelectedIndex();

        int[] row_indexes = alunoCampo.getSelectedRows();
        String alunos[][] = new String[row_indexes.length][3];
        for (int i = 0; i < row_indexes.length; i++) {
            alunos[i][0] = alunoCampo.getValueAt(row_indexes[i], 0).toString();
            alunos[i][1] = alunoCampo.getValueAt(row_indexes[i], 1).toString();
            alunos[i][2] = alunoCampo.getValueAt(row_indexes[i], 2).toString();
        }

        try {
            if (turma_em_edicao == null) {
                controle.cadastrarTurma(nome, indice_educador, indice_auxiliar, tipo, sala, turno, alunos);
            } else {
                controle.editarTurma(turma_em_edicao, nome, indice_educador, indice_auxiliar, tipo, sala, turno, alunos);
            }

            dispose();
        } catch (PreenchimentoCamposException ex) {
            PopUps.showMessage(ex.getMessage());
        } catch (FuncionarioNaoDisponivelException ex) {
            PopUps.showMessage(ex.getMessage());
        } catch (TurmaLotadaException ex) {
            PopUps.showMessage(ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

